let heatmapHisp: google.maps.visualization.HeatmapLayer; 

function initMap(): void {
  map = new google.maps.Map(document.getElementById("map") as HTMLElement, {
    zoom: 13,
    center: { lat: 42.6370667, lng: -71.3153136 },
    mapTypeId: "satellite",
  });

  heatmapAll = new google.maps.visualization.HeatmapLayer({
    data: getPoints("ALL")
  });
  heatmapBlack = new google.maps.visualization.HeatmapLayer({
    data: getPoints("BLACK"),
  });
  heatmapAsian = new google.maps.visualization.HeatmapLayer({
    data: getPoints("ASIAN"),
  });
  heatmapWhite = new google.maps.visualization.HeatmapLayer({
    data: getPoints("WHITE"),
  });
  heatmapHisp = new google.maps.visualization.HeatmapLayer({
    data: getPoints("LATINO"),
  });

  heatmapAll.set("radius",30)
  heatmapBlack.set("radius",30)
  heatmapAsian.set("radius",30)
  heatmapWhite.set("radius",30)
  heatmapHisp.set("radius",30)
  heatmapAll.setMap(map)

  document
    .getElementById("all")!
    .addEventListener("click", toggleHeatmapAll);
  document
    .getElementById("black")!
    .addEventListener("click", toggleHeatmapBlack);
  document
    .getElementById("white")!
    .addEventListener("click", toggleHeatmapWhite);
  document
    .getElementById("asian")!
    .addEventListener("click", toggleHeatmapAsian);
  document
    .getElementById("clear")!
    .addEventListener("click", clear);
  document
    .getElementById("hisp")!
    .addEventListener("click", toggleHeatmapHisp);
}

function clear(){
    heatmapAll.setMap(null)
    heatmapBlack.setMap(null)
    heatmapWhite.setMap(null)
    heatmapAsian.setMap(null)
    heatmapHisp.setMap(null)
}

